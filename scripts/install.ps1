$ErrorActionPreference = "Stop"

$root = Split-Path -Parent $PSScriptRoot
$envFile = Join-Path $root ".env"
$baseCompose = Join-Path $root "docker-compose.production.yml"
$prodOverride = Join-Path $root "docker-compose.prod.yml"
$ipOverride = Join-Path $root "docker-compose.ip.yml"

function Require-Command([string]$Name, [string]$Hint) {
  if (-not (Get-Command $Name -ErrorAction SilentlyContinue)) {
    throw "[install] missing required command: $Name`n[install] hint: $Hint"
  }
}

function Normalize-ServerInput([string]$Value) {
  $normalized = $Value.Trim()
  $normalized = $normalized -replace '^https?://', ''
  $normalized = $normalized -replace '/.*$', ''
  $normalized = $normalized.Trim('/').Trim()
  return $normalized
}

function New-RandomHex([int]$BytesLength) {
  $bytes = New-Object byte[] $BytesLength
  $rng = [System.Security.Cryptography.RandomNumberGenerator]::Create()
  $rng.GetBytes($bytes)
  $rng.Dispose()
  return -join ($bytes | ForEach-Object { $_.ToString("x2") })
}

function New-RandomBase64([int]$BytesLength) {
  $bytes = New-Object byte[] $BytesLength
  $rng = [System.Security.Cryptography.RandomNumberGenerator]::Create()
  $rng.GetBytes($bytes)
  $rng.Dispose()
  return [Convert]::ToBase64String($bytes)
}

function Wait-HttpReady([string]$Url, [hashtable]$Headers = $null) {
  for ($i = 0; $i -lt 45; $i++) {
    try {
      if ($Headers) {
        Invoke-WebRequest -Uri $Url -Headers $Headers -UseBasicParsing -TimeoutSec 5 | Out-Null
      }
      else {
        Invoke-WebRequest -Uri $Url -UseBasicParsing -TimeoutSec 5 | Out-Null
      }
      return $true
    }
    catch {
      Start-Sleep -Seconds 2
    }
  }
  return $false
}

Require-Command "docker" "Install Docker Desktop or Docker Engine"
Require-Command "curl" "Install curl"
docker info | Out-Null

$composeMode = "docker-compose-plugin"
try {
  docker compose version | Out-Null
}
catch {
  if (Get-Command "docker-compose" -ErrorAction SilentlyContinue) {
    $composeMode = "docker-compose-legacy"
  }
  else {
    throw "[install] Docker Compose is missing (neither 'docker compose' nor 'docker-compose' found)."
  }
}

function Invoke-Compose {
  param(
    [Parameter(ValueFromRemainingArguments = $true)]
    [string[]]$Args
  )
  if ($composeMode -eq "docker-compose-legacy") {
    & docker-compose @Args
  }
  else {
    & docker compose @Args
  }
}

if (-not (Test-Path $baseCompose)) {
  throw "[install] missing compose file: $baseCompose"
}
if (-not (Test-Path $prodOverride)) {
  throw "[install] missing compose override file: $prodOverride"
}
if (-not (Test-Path $ipOverride)) {
  throw "[install] missing compose override file: $ipOverride"
}

$serverInput = Read-Host "Enter domain or IP"
$serverHost = Normalize-ServerInput $serverInput
if ([string]::IsNullOrWhiteSpace($serverHost)) {
  throw "[install] empty host value, aborting"
}

$parsedIp = $null
$isIp = [System.Net.IPAddress]::TryParse($serverHost, [ref]$parsedIp)
$mode = if ($isIp) { "ip" } else { "domain" }

if ($mode -eq "domain") {
  $defaultEmail = "admin@$serverHost"
  $emailInput = Read-Host "Email for TLS certificate [$defaultEmail]"
  $acmeEmail = if ([string]::IsNullOrWhiteSpace($emailInput)) { $defaultEmail } else { $emailInput.Trim() }
  $tlsEnabled = "true"
  $apiBase = "https://$serverHost"
  $wsUrl = "wss://$serverHost/ws"
  $relayPublishAddress = "127.0.0.1"
  $webPublishAddress = "127.0.0.1"
  $webPublishPort = "8081"
  $webAllowedOrigins = "https://$serverHost"
  $webTrustProxyHeaders = "true"
  $siteUrl = "https://$serverHost"
}
else {
  $acmeEmail = "install@localhost"
  $tlsEnabled = "false"
  $apiBase = "http://$serverHost:8080"
  $wsUrl = "ws://$serverHost:8080/ws"
  $relayPublishAddress = "0.0.0.0"
  $webPublishAddress = "0.0.0.0"
  $webPublishPort = "80"
  $webAllowedOrigins = "http://$serverHost"
  $webTrustProxyHeaders = "false"
  $siteUrl = "http://$serverHost"
}

$postgresDb = "secure_messenger"
$postgresUser = "secure_messenger"
$postgresPassword = New-RandomHex 16
$authTokenPepper = New-RandomHex 32
$securityEncryptionKey = New-RandomBase64 32
$databaseUrl = "postgres://{0}:{1}@postgres:5432/{2}?sslmode=disable" -f $postgresUser, $postgresPassword, $postgresDb
$transportAlternates = "$apiBase/api/v1/sync/poll"

if (Test-Path $envFile) {
  $backupPath = "$envFile.backup.$((Get-Date).ToString('yyyyMMddHHmmss'))"
  Copy-Item -Path $envFile -Destination $backupPath -Force
  Write-Host "[install] existing .env backed up to $backupPath"
}

$envContent = @"
