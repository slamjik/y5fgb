[System.IO.File]::WriteAllText($envFile, $envContent, [System.Text.UTF8Encoding]::new($false))
Write-Host "[install] .env generated: $envFile"

if ($mode -eq "domain") {
  $composeOverride = $prodOverride
}
else {
  $composeOverride = $ipOverride
}

$composeArgs = @("-f", $baseCompose, "-f", $composeOverride, "--env-file", $envFile)

Write-Host "[install] removing old orphan containers (if any)"
try {
  Invoke-Compose @composeArgs down --remove-orphans | Out-Null
}
catch {
  # Best effort cleanup.
}

if ($mode -eq "domain") {
  Write-Host "[install] starting stack in DOMAIN mode (TLS via Caddy)"
  Invoke-Compose @composeArgs up -d --build --remove-orphans
  $healthUrl = "https://$serverHost/health"
  $readyUrl = "https://$serverHost/ready"
  $configUrl = "https://$serverHost/api/v1/config"
  $siteHealthUrl = "https://$serverHost/"
  $fallbackHealthUrl = "http://127.0.0.1/health"
}
else {
  Write-Host "[install] starting stack in IP mode (HTTP + Web UI + API)"
  Invoke-Compose @composeArgs up -d --build --remove-orphans
  $healthUrl = "http://$serverHost:8080/health"
  $readyUrl = "http://$serverHost:8080/ready"
  $configUrl = "http://$serverHost:8080/api/v1/config"
  $siteHealthUrl = "http://$serverHost/healthz"
  $fallbackHealthUrl = $null
}

Write-Host "[install] waiting for health: $healthUrl"
$healthOk = Wait-HttpReady -Url $healthUrl
if (-not $healthOk -and $mode -eq "domain") {
  $healthOk = Wait-HttpReady -Url $fallbackHealthUrl -Headers @{ Host = $serverHost }
  if ($healthOk) {
    Write-Host "[install] public health check is not reachable yet, but local edge check passed."
  }
}

if (-not $healthOk) {
  Invoke-Compose @composeArgs ps
  Invoke-Compose @composeArgs logs --tail=150
  throw "[install] health check failed"
}

if (-not (Wait-HttpReady -Url $readyUrl)) {
  Write-Warning "[install] warning: readiness endpoint is not reachable yet at $readyUrl"
}
if (-not (Wait-HttpReady -Url $configUrl)) {
  Write-Warning "[install] warning: /api/v1/config is not reachable yet at $configUrl"
}
if (-not (Wait-HttpReady -Url $siteHealthUrl)) {
  Write-Warning "[install] warning: web UI endpoint is not reachable yet at $siteHealthUrl"
}

Invoke-Compose @composeArgs ps

Write-Host ""
Write-Host "=============================="
Write-Host "SERVER READY"
Write-Host ""
Write-Host "Site:"
Write-Host $siteUrl
Write-Host ""
Write-Host "API:"
Write-Host $apiBase
Write-Host ""
Write-Host "Web client connects to server automatically"
Write-Host "=============================="
